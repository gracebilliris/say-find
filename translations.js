// Get current language (default to English)
let currentLanguage = localStorage.getItem('sayFindLanguage') || 'en';

// Function to get translated text
function t(key) {
    if (TRANSLATIONS[currentLanguage] && TRANSLATIONS[currentLanguage][key]) {
        return TRANSLATIONS[currentLanguage][key];
    }
    // Fallback to English if translation not found
    return TRANSLATIONS['en'][key] || key;
}

// Function to set language
function setLanguage(lang) {
    if (LANGUAGES[lang]) {
        currentLanguage = lang;
        localStorage.setItem('sayFindLanguage', lang);
        updatePageLanguage();
    }
}

// Function to update all UI text based on language
function updatePageLanguage() {
    // Update all elements with data-translate attribute
    document.querySelectorAll('[data-translate]').forEach(el => {
        const key = el.getAttribute('data-translate');
        el.textContent = t(key);
    });

    // Update placeholders
    const teamNameInput = document.getElementById('teamNameInput');
    if (teamNameInput) {
        teamNameInput.placeholder = t('teamNameOptional');
    }
}
